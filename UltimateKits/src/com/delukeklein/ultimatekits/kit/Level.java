package com.delukeklein.ultimatekits.kit;

public final class Level {

	private int price;
	
	private String name;

	public void setPrice(int price) {
		this.price = price;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getPrice() {
		return price;
	}

	public String getName() {
		return name;
	}

}
